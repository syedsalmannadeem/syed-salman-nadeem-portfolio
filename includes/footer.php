<script>
    window.addEventListener("load", () => {
    setTimeout(() => {
        const headings = document.querySelectorAll(".typewriter-heading");
        const wordsArray = ["CREATIVE", "INNOVATIVE", "VISIONARY", "DYNAMIC", "ICONIC"]; // Add more words here
        headings.forEach((heading) => {
            initTypewriter(heading.querySelector(".typewriter"), wordsArray);
        });

        function initTypewriter(element, wordsArray) {
            let isDeleting = false;
            let index = 0;
            let wordIndex = 0;

            function type() {
                const currentWord = wordsArray[wordIndex];
                const currentText = currentWord.substring(0, index);
                element.textContent = currentText;

                if (!isDeleting) {
                    index++;
                    if (index > currentWord.length) {
                        isDeleting = true;
                        setTimeout(type, 1000); // Delay before starting to delete
                        return;
                    }
                } else {
                    index--;
                    if (index === 0) {
                        isDeleting = false;
                        wordIndex = (wordIndex + 1) % wordsArray.length; // Cycle to the next word
                    }
                }

                const typingSpeed = isDeleting ? 100 : 200; // Adjust typing and deleting speed here
                setTimeout(type, typingSpeed);
            }

            type();
        }
    }, 5000); // Delay the execution by 5000 milliseconds (5 seconds)
});
</script>
